package com.Model;

public class Carrier {
    private String carrierName;
    private int d30, d60, d90, bulk;
    private int r2, r10, r20;
    private int silver, gold, platinum;

    public String getCarrierName() { return carrierName; }
    public void setCarrierName(String carrierName) { this.carrierName = carrierName; }

    public int getD30() { return d30; }
    public void setD30(int d30) { this.d30 = d30; }

    public int getD60() { return d60; }
    public void setD60(int d60) { this.d60 = d60; }

    public int getD90() { return d90; }
    public void setD90(int d90) { this.d90 = d90; }

    public int getBulk() { return bulk; }
    public void setBulk(int bulk) { this.bulk = bulk; }

    public int getR2() { return r2; }
    public void setR2(int r2) { this.r2 = r2; }

    public int getR10() { return r10; }
    public void setR10(int r10) { this.r10 = r10; }

    public int getR20() { return r20; }
    public void setR20(int r20) { this.r20 = r20; }

    public int getSilver() { return silver; }
    public void setSilver(int silver) { this.silver = silver; }

    public int getGold() { return gold; }
    public void setGold(int gold) { this.gold = gold; }

    public int getPlatinum() { return platinum; }
    public void setPlatinum(int platinum) { this.platinum = platinum; }
}
